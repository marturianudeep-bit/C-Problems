#include <stdio.h>

int main() {
    int num1, num2, sum;

    // Taking input
    printf("Enter first number: ");
    scanf("%d", &num1);

    printf("Enter second number: ");
    scanf("%d", &num2);

    // Calculating sum
    sum = num1 + num2;

    // Displaying result
    printf("Sum = %d\n", sum);

    return 0;
}
